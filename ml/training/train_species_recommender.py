#!/usr/bin/env python3
"""
HeatWise Species Recommender — Rule-Based ML Training
======================================================
Trains three complementary models from the rule-based species catalog:

  1. species_scorer    — GBT regressor predicting species relevance score (0–1)
                         given scenario context (sun, water, wind, purpose, etc.)
  2. species_ranker    — GBT pairwise ranker distinguishing good vs bad fit
                         for a given scenario (binary preference label)
  3. heat_scorer       — GBT regressor predicting cooling contribution score
                         from species traits + scenario heat context

Synthetic training data is generated by applying the rule-based scoring formulas
directly to every (scenario, species) combination. The models learn to generalise
these rules to unseen species/scenario combinations and can be blended with the
TypeScript catalog-hybrid fallback.

Usage
-----
  python training/train_species_recommender.py \\
      --species-csv data/bootstrap/sample_outputs/demo_pack/species_features.csv \\
      --out-dir registry/artifacts \\
      --registry-dir registry \\
      [--n-scenarios 4000] [--seed 42]

Output
------
  registry/artifacts/species_scorer/     model.joblib + metadata.json
  registry/artifacts/species_ranker/     model.joblib + metadata.json
  registry/artifacts/heat_scorer/        model.joblib + metadata.json
  registry/registry_index.json           updated with all 3 entries
"""

