SCENARIO_FEATURE_COLS = [
    "sun_ord", "water_avail_ord", "wind_ord", "heat_ord",
    "purpose_cooling", "purpose_food", "purpose_aesthetic",
    "purpose_privacy", "purpose_biodiversity", "purpose_mixed",
    "maint_pref_ord", "pet_safe_required", "edible_preferred",
    "floor_level_norm", "area_m2_norm",
    "space_balcony", "space_indoor", "space_rooftop", "space_terrace",
]

def encode_scenario(sc: dict) -> dict:
    sun_ord   = {"shade": 0, "partial": 1, "full": 2}[sc["sun"]]
    water_ord = {"scarce": 0, "moderate": 1, "plentiful": 2}[sc["water_availability"]]
    wind_ord  = {"low": 0, "medium": 1, "high": 2}[sc["wind_level"]]
    heat_ord  = {"low": 0, "moderate": 1, "high": 2, "extreme": 3}[sc["heat_exposure"]]
    maint_ord = {"minimal": 0, "low": 0, "moderate": 1, "high": 2}[sc["maintenance_preference"]]
    purp = sc["purpose_primary"]
    return {
        "sun_ord":          sun_ord,
        "water_avail_ord":  water_ord,
        "wind_ord":         wind_ord,
        "heat_ord":         heat_ord,
        "maint_pref_ord":   maint_ord,
        "pet_safe_required":sc["pet_safe_required"],
        "edible_preferred": sc["edible_preferred"],
        "floor_level_norm": min(1.0, sc["floor_level"] / 25.0),
        "area_m2_norm":     min(1.0, sc["area_m2"] / 80.0),
        "purpose_cooling":    int(purp == "cooling"),
        "purpose_food":       int(purp == "food"),
        "purpose_aesthetic":  int(purp == "aesthetic"),
        "purpose_privacy":    int(purp == "privacy"),
        "purpose_biodiversity": int(purp == "biodiversity"),
        "purpose_mixed":      int(purp == "mixed"),
        "space_balcony":  int(sc["space_type"] == "outdoor_balcony"),
        "space_indoor":   int(sc["space_type"] == "indoor"),
        "space_rooftop":  int(sc["space_type"] == "outdoor_rooftop"),
        "space_terrace":  int(sc["space_type"] == "outdoor_terrace"),
    }


def build_regression_dataset(
    species_df: pd.DataFrame,
    scenarios: list[dict],
) -> tuple[pd.DataFrame, np.ndarray, np.ndarray]:
    """Cross-product of scenarios × species, returns X, y_relevance, y_heat."""
    rows, y_rel, y_heat = [], [], []
    for sc in scenarios:
        sc_feat = encode_scenario(sc)
        for _, sp in species_df.iterrows():
            rel, ht = rule_score(sp, sc)
            row = {**sc_feat}
            for col in SPECIES_FEATURE_COLS:
                row[col] = float(sp[col])
            rows.append(row)
            y_rel.append(rel)
            y_heat.append(ht)

    X = pd.DataFrame(rows)
    return X, np.array(y_rel, dtype=np.float32), np.array(y_heat, dtype=np.float32)


def build_pairwise_dataset(
    species_df: pd.DataFrame,
    scenarios: list[dict],
    rng: random.Random,
    pairs_per_scenario: int = 6,
) -> tuple[pd.DataFrame, np.ndarray]:
    """
    For each scenario, sample pairs (spA, spB) where spA has higher rule score.
    Label = 1 if spA features are presented as 'preferred'.
    Features = [scenario, spA_features, spB_features] — diff-encoding.
    """
    rows, labels = [], []
    n_sp = len(species_df)
    sp_list = species_df.reset_index(drop=True)

    for sc in scenarios:
        sc_feat = encode_scenario(sc)
        # Pre-compute scores for all species in this scenario
        scores = []
        for _, sp in sp_list.iterrows():
            r, _ = rule_score(sp, sc)
            scores.append(r)
        scores = np.array(scores)

        for _ in range(pairs_per_scenario):
            ia, ib = rng.sample(range(n_sp), 2)
            ra, rb = scores[ia], scores[ib]
            # Skip ties
            if abs(ra - rb) < 0.05:
                continue
            # Ensure spA is the preferred (higher score)
            if ra < rb:
                ia, ib = ib, ia
                ra, rb = rb, ra

            spa = sp_list.iloc[ia]
            spb = sp_list.iloc[ib]
            row = {}
            for k, v in sc_feat.items():
                row[f"sc_{k}"] = v
            for col in SPECIES_FEATURE_COLS:
                row[f"a_{col}"] = float(spa[col])
                row[f"b_{col}"] = float(spb[col])
                row[f"d_{col}"] = float(spa[col]) - float(spb[col])
            row["score_diff"] = float(ra - rb)
            rows.append(row)
            labels.append(1)
            # Flip pair → negative example
            row2 = {}
            for k, v in sc_feat.items():
                row2[f"sc_{k}"] = v
            for col in SPECIES_FEATURE_COLS:
                row2[f"a_{col}"] = float(spb[col])
                row2[f"b_{col}"] = float(spa[col])
                row2[f"d_{col}"] = float(spb[col]) - float(spa[col])
            row2["score_diff"] = float(rb - ra)
            rows.append(row2)
            labels.append(0)

    X = pd.DataFrame(rows)
    return X, np.array(labels, dtype=np.int32)


