def train_regressor(X_tr, y_tr, X_val, y_val, name: str):
    print(f"\n[{name}] training GBT regressor — {len(X_tr)} train / {len(X_val)} val rows …")
    model = GradientBoostingRegressor(
        n_estimators=200,
        max_depth=5,
        learning_rate=0.05,
        subsample=0.85,
        min_samples_leaf=10,
        random_state=42,
        validation_fraction=0.1,
        n_iter_no_change=20,
        tol=1e-4,
    )
    model.fit(X_tr, y_tr)
    pred_tr  = model.predict(X_tr)
    pred_val = model.predict(X_val)
    metrics = {
        "train_mae":  float(mean_absolute_error(y_tr, pred_tr)),
        "train_rmse": float(mean_squared_error(y_tr, pred_tr) ** 0.5),
        "val_mae":    float(mean_absolute_error(y_val, pred_val)),
        "val_rmse":   float(mean_squared_error(y_val, pred_val) ** 0.5),
        "n_estimators_used": int(model.n_estimators_),
    }
    print(f"  train MAE={metrics['train_mae']:.4f} RMSE={metrics['train_rmse']:.4f} "
          f"| val MAE={metrics['val_mae']:.4f} RMSE={metrics['val_rmse']:.4f}")
    return model, metrics


def train_classifier(X_tr, y_tr, X_val, y_val, name: str):
    print(f"\n[{name}] training GBT classifier — {len(X_tr)} train / {len(X_val)} val rows …")
    model = GradientBoostingClassifier(
        n_estimators=200,
        max_depth=5,
        learning_rate=0.05,
        subsample=0.85,
        min_samples_leaf=10,
        random_state=42,
        validation_fraction=0.1,
        n_iter_no_change=20,
        tol=1e-4,
    )
    model.fit(X_tr, y_tr)
    pred_val  = model.predict(X_val)
    prob_val  = model.predict_proba(X_val)[:, 1]
    metrics = {
        "val_accuracy": float(accuracy_score(y_val, pred_val)),
        "val_roc_auc":  float(roc_auc_score(y_val, prob_val)),
        "n_estimators_used": int(model.n_estimators_),
    }
    print(f"  val accuracy={metrics['val_accuracy']:.4f} AUC={metrics['val_roc_auc']:.4f}")
    return model, metrics


