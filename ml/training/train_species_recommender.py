def save_model(
    model,
    metrics: dict,
    feature_cols: list[str],
    task: str,
    model_name: str,
    out_dir: Path,
    hyperparams: dict,
    n_train: int,
    n_val: int,
    species_csv_path: str,
) -> dict:
    model_dir = out_dir / model_name
    model_dir.mkdir(parents=True, exist_ok=True)
    model_path = model_dir / "model.joblib"
    meta_path  = model_dir / "metadata.json"

    joblib.dump({"model": model, "feature_cols": feature_cols}, model_path)

    meta = {
        "model_name":    model_name,
        "task":          task,
        "version":       "v1.0",
        "trained_at":    datetime.now(timezone.utc).isoformat(),
        "n_train":       n_train,
        "n_val":         n_val,
        "feature_cols":  feature_cols,
        "metrics":       metrics,
        "hyperparams":   hyperparams,
        "data_sources":  ["rule_based_species_catalog", species_csv_path],
        "framework":     "sklearn.GradientBoosting",
        "notes":         "Trained on synthetic (scenario × species) data generated from rule-based scoring logic.",
    }
    meta_path.write_text(json.dumps(meta, indent=2))
    print(f"  → saved: {model_path}")
    return {
        "model_path": str(model_path),
        "meta_path":  str(meta_path),
        "metrics":    metrics,
        "feature_cols": feature_cols,
        "task":       task,
        "model_name": model_name,
        "version":    "v1.0",
    }


def update_registry(registry_dir: Path, entries: list[dict], species_csv_path: str):
    idx_path = registry_dir / "registry_index.json"
    if idx_path.exists():
        data = json.loads(idx_path.read_text())
    else:
        data = {"models": []}

    now = datetime.now(timezone.utc).isoformat()
    for e in entries:
        record = {
            "model_id":              f"{e['model_name']}-v1",
            "model_name":            e["model_name"],
            "task":                  e["task"],
            "version":               e["version"],
            "status":                "production",
            "trained_at":            now,
            "training_snapshot_id":  "rule_based_species_v1",
            "artifact_paths": {
                "model":    e["model_path"],
                "metadata": e["meta_path"],
            },
            "training_code_version": "train_species_recommender.py@v1",
            "data_sources_used":     ["rule_based_species_catalog", species_csv_path],
            "source_mix_summary": {
                "synthetic_rule_based_pct": 100,
                "live_telemetry_pct":       0,
            },
            "metrics_summary":  e["metrics"],
            "feature_count":    len(e["feature_cols"]),
            "hyperparams": {
                "n_estimators": 200,
                "max_depth":    5,
                "learning_rate": 0.05,
            },
        }
        # Replace or append
        existing = [m for m in data["models"] if m["model_name"] == e["model_name"]]
        if existing:
            idx = data["models"].index(existing[0])
            data["models"][idx] = record
        else:
            data["models"].append(record)

    idx_path.write_text(json.dumps(data, indent=2))
    print(f"\n✓ Registry updated: {idx_path}  ({len(data['models'])} models total)")


