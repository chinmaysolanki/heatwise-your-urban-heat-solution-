CLIMATE_ORDER = ["HOT_HUMID", "HOT_DRY", "MONSOON_HEAVY", "TEMPERATE_LITE"]
WATER_ORDER   = {"LOW": 0, "MED": 1, "HIGH": 2}
SUN_ORDER     = {"SHADE": 0, "PART": 1, "FULL": 2}
MAINT_ORDER   = {"LOW": 0, "MED": 1, "HIGH": 2}
ROOT_ORDER    = {"LOW": 0, "MED": 1, "HIGH": 2}
SAFETY_ORDER  = {"SAFE": 2, "CAUTION": 1, "UNSAFE": 0}
NATIVE_ORDER  = {"LOW": 0, "MED": 1, "HIGH": 2}
CONT_ORDER    = {"EXCELLENT": 3, "GOOD": 2, "FAIR": 1, "POOR": 0}


def load_species(csv_path: Path) -> pd.DataFrame:
    df = pd.read_csv(csv_path)

    # Expand pipe-separated climate_suitability → one-hot per climate
    for c in CLIMATE_ORDER:
        df[f"climate_{c.lower()}"] = df["climate_suitability"].str.contains(c, na=False).astype(int)

    # Ordinal encode
    df["water_demand_ord"]         = df["water_demand"].map(WATER_ORDER).fillna(1).astype(int)
    df["sunlight_pref_ord"]        = df["sunlight_preference"].map(SUN_ORDER).fillna(1).astype(int)
    df["maintenance_ord"]          = df["maintenance_need"].map(MAINT_ORDER).fillna(1).astype(int)
    df["root_aggressiveness_ord"]  = df["root_aggressiveness"].map(ROOT_ORDER).fillna(1).astype(int)
    df["child_pet_safety_ord"]     = df["child_pet_safety"].map(SAFETY_ORDER).fillna(1).astype(int)
    df["native_support_ord"]       = df["native_support"].map(NATIVE_ORDER).fillna(1).astype(int)
    df["container_suitability_ord"]= df["container_suitability"].map(CONT_ORDER).fillna(2).astype(int)

    # Growth-habit one-hot
    habits = ["HERB", "SHRUB", "UPRIGHT", "VINE", "GROUND_COVER",
              "TRAILING", "SUCCULENT", "GRASS"]
    for h in habits:
        df[f"habit_{h.lower()}"] = (df["growth_habit"] == h).astype(int)

    # Cast numeric columns
    for col in ["pollinator_value", "cooling_contribution", "privacy_contribution",
                "edible"]:
        df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0).astype(float)

    return df


