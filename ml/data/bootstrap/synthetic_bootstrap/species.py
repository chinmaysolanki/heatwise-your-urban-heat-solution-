def get_species_library() -> tuple[SpeciesSpec, ...]:
    """Species rows used by the engine (set via :func:`set_species_library`)."""
    return _ACTIVE


def set_species_library(lib: tuple[SpeciesSpec, ...]) -> None:
    """Replace the active library (typically loaded from ``species_library.json``)."""
    global _ACTIVE
    if not lib:
        raise ValueError("species library must be non-empty")
    _ACTIVE = lib
